# 2) მომხმარებელს შემოატანინეთ სამი წინადადება და თითოეულზე გამოიყენეთ სხვადასხვა სტრინგის მეთოდი:
# პირველ წინადადებაზე — .lower()
# მეორე წინადადებაზე — .upper()
# მესამე წინადადებაზე — .capitalize()

sentence1 = input("შეიყვანეთ პირველი წინადადება: ")
sentence2 = input("შეიყვანეთ მეორე წინადადება: ")
sentence3 = input("შეიყვანეთ მესამე წინადადება: ")

# სტრინგის მეთოდების გამოყენება
print("პირველი წინადადება (lower):", sentence1.lower())
print("მეორე წინადადება (upper):", sentence2.upper())
print("მესამე წინადადება (capitalize):", sentence3.capitalize())
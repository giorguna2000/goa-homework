# 6)მომხარებელს შემოატანინეთ რიცხვი და შეინახეთ ცვლადში, შემდეგ კი 0-დან შემოტანილი რიცხვის ჩათვლით შეამოწმეთ, თუ ლუწია დაბეჭდეთ რიცხვი is Even, სხვა შემთხვევაში რიცხვი is Odd;(მაგალითად 4, ლუწია ამიტომაც დაბეჭდავთ "4 is Even")
num = int(input("Enter your number: "))
i = 0

while i <= num:
    if i % 2 == 0:
        print(f"{i} is Even")
    else:
        print(f"{i} is Odd")
    i += 1

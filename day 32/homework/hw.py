# 5) შექმენით ფუნქცია და გადაეცით ორი რიცხვი პარამეტრებად. ფუქნციამ უნდა დააბრუნოს პირველი რიცხვი აყვანილი მეორე რიცხვის ხარისხში

def power(base, exponent):
    return base ** exponent
print(power(2, 3))  
print(power(5, 2))   
